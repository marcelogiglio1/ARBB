document.addEventListener("DOMContentLoaded", () => {
  let currentTournament = false; // false = SuperLiga, true = 1ra Femenina

  function updateButtonColors() {
      const buttons = document.querySelectorAll(".stat-button");
      const color = currentTournament ? "rgb(237, 50, 55)" : "rgb(1, 44, 122)";
      
      buttons.forEach(button => {
          button.style.backgroundColor = color;
      });
  }

  function populateTable(stat, tableId) {
      const tbody = document.getElementById(tableId);
      tbody.innerHTML = "";
      
      const topPlayers = players
          .filter(player => player.tournament === currentTournament)
          .map(player => ({
              name: player.name,
              average: (player[stat] / (player.games_played || 1)).toFixed(2) // Calcular promedio por juego
          }))
          .sort((a, b) => b.average - a.average)
          .slice(0, 10);
      
      let tableContent = `<thead>
          <tr>
              <th>Jugador</th>
              <th>${stat.charAt(0).toUpperCase() + stat.slice(1)}</th>
          </tr>
      </thead>
      <tbody>`;
      
      topPlayers.forEach(player => {
          tableContent += `<tr>
              <td>${player.name}</td>
              <td>${player.average}</td>
          </tr>`;
      });
      
      tableContent += "</tbody>";
      tbody.innerHTML = tableContent;
  }

  function showTable(tableId, stat) {
      document.querySelectorAll(".stat-table").forEach(table => table.classList.add("hidden"));
      document.getElementById(tableId).classList.remove("hidden");
      populateTable(stat, tableId);
  }

  function filterTournament(tournament) {
      currentTournament = tournament;
      updateButtonColors();
      showTable("pointsTable", "points"); // Refrescar con la tabla de puntos por defecto
  }

  document.getElementById("pointsButton").addEventListener("click", () => showTable("pointsTable", "points"));
  document.getElementById("reboundsButton").addEventListener("click", () => showTable("reboundsTable", "rebounds"));
  document.getElementById("assistsButton").addEventListener("click", () => showTable("assistsTable", "assists"));
  document.getElementById("superligaButton").addEventListener("click", () => filterTournament(false));
  document.getElementById("femeninaButton").addEventListener("click", () => filterTournament(true));
  
  updateButtonColors();
  showTable("pointsTable", "points");
});


