    function getLeaders(players) {
        const malePlayers = players.filter(p => !p.tournament);
        const femalePlayers = players.filter(p => p.tournament);

        return {
            femenino: {
                puntos: getTopPlayer(femalePlayers, "points" ),
                rebotes: getTopPlayer(femalePlayers, "rebounds"),
                asistencias: getTopPlayer(femalePlayers, "assists")
            },
            masculino: {
                puntos: getTopPlayer(malePlayers, "points"),
                rebotes: getTopPlayer(malePlayers, "rebounds"),
                asistencias: getTopPlayer(malePlayers, "assists")
            }
        };
    }

    function getTopPlayer(players, stat) {
      return players.reduce((max, player) => {
          const avgStat = player.games_played > 0 ? player[stat] / player.games_played : 0;
          const maxAvgStat = max.games_played > 0 ? max[stat] / max.games_played : 0;
          return avgStat > maxAvgStat ? player : max;
      }, players[0]);
  }

